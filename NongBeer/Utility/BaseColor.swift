import UIKit
import EZSwiftExtensions
extension UIColor {

    class func lightPrimary() -> UIColor {
        return UIColor(hexString: "ffcd5f")!
    }
    
    class func primary() -> UIColor {
        return UIColor(hexString: "ffbc00")!
    }
    
    class func darkPrimary() -> UIColor {
        return UIColor(hexString: "f7931e")!
    }
    
    class func accent() -> UIColor {
        return UIColor(hexString: "00a8ba")!
    }
    
    class func darkAccent() -> UIColor {
        return UIColor(hexString: "009299")!
    }
    
    class func red() -> UIColor {
        return UIColor(hexString: "f7213b")!
    }
    
    class func lightGray() -> UIColor {
        return UIColor(hexString: "f2f2f2")!
    }
    
    class func gray() -> UIColor {
        return UIColor(hexString: "cccccc")!
    }
    
    class func darkGray() -> UIColor {
        return UIColor(hexString: "808080")!
    }
    
    class func blackAlpha() -> UIColor {
        return UIColor(hexString: "333333")!
    }
    
    class func green() -> UIColor {
        return UIColor(hexString: "46d36b")!
    }
}
